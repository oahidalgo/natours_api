const Tour = require('./../models/tourModel');

// Route handler for retrieving all tours.
exports.getAllTours = async (req, res) => {
  try {
    //Getting all the tours from mongodb
    const tours = await Tour.find();
    res.status(200).json({
      status: 'success',
      results: tours.length,
      data: {
        tours,
      },
    });
  } catch (err) {
    res.status(404).json({
      status: 'fail',
      message: err,
    });
  }
};

// Route handler for retrieving a specific tour by ID.
exports.getTour = async (req, res) => {
  try {
    //fn to get specific tour by ID
    const tour = await Tour.findById(req.params.id);
    res.status(200).json({
      status: 'success',
      data: {
        tour,
      },
    });
  } catch (err) {
    res.status(404).json({
      status: 'fail',
      message: err,
    });
  }
};

// Route handler for creating a new tour.
exports.createTour = async (req, res) => {
  try {
    //Creating a new tour in the database
    const newTour = await Tour.create(req.body);
    res.status(201).json({
      status: 'success',
      data: {
        tour: newTour,
      },
    });
  } catch (err) {
    res.status(400).json({
      status: 'fail',
      message: 'Invalid data sent!',
    });
  }
};

// Route handler for updating a specific tour by ID.
exports.updateTour = async (req, res) => {
  try {
    //new flag is gonna return the newly created tour
    const tour = await Tour.findByIdAndUpdate(req.params.id, req.body, {
      new: true,
      runValidators: true,
    });
    res.status(200).json({
      status: 'success',
      data: {
        tour,
      },
    });
  } catch (err) {
    res.status(400).json({
      status: 'fail',
      message: 'Invalid data sent!',
    });
  }
};

// Route handler for deleting a specific tour by ID.
exports.deleteTour = async (req, res) => {
  try {
    //not send back data on deletion
    await Tour.findByIdAndDelete(req.params.id);
    res.status(204).json({
      status: 'success',
      data: null,
    });
  } catch (err) {
    res.status(400).json({
      status: 'fail',
      message: 'Invalid data sent!',
    });
  }
};
